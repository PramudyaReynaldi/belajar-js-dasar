// ======================= Function Anonim ============================
const menghitungLuasPersegi = function(panjang, lebar) {
    return panjang * lebar;
}
const hasilPersegi = menghitungLuasPersegi(10, 10);
console.log(hasilPersegi); // Output: 100




let pi = 3.14;
const luasLingkaran = function(r) {
    return pi * r * r;
}
const hasilLuasLingkaran = luasLingkaran(20);
console.log(hasilLuasLingkaran);